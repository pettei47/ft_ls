#include "libft.h"

int	ft_strchr_cnt(char *s, char c)
{
	int	i;
	int	ret;

	ret = 0;
	i = -1;
	while (s && s[++i])
	{
		if (s[i] == c)
			ret++;
	}
	return (ret);
}
