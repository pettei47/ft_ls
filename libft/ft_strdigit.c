#include "libft.h"

int	ft_strdigit(char *s)
{
	long	i;
	long	sign;

	i = 0;
	sign = 0;
	if (s[i] == '-' || s[i] == '+')
	{
		i++;
		sign = 1;
	}
	while (s[i])
	{
		if (ft_isdigit(s[i++]) == 0)
			return (0);
	}
	if (i == 0 || (i == 1 && sign == 1))
		return (0);
	return (1);
}
